CREATE TABLE IF NOT EXISTS EventLog
(

    LOGIN_ID   INT AUTO_INCREMENT PRIMARY KEY,
    USER_ID    INT NOT NULL,
    EVENT_DATE DATE,
    CHECK_IN   TIME,
    CHECK_OUT  TIME,
    FOREIGN KEY (USER_ID) REFERENCES User (USER_ID)


    );




